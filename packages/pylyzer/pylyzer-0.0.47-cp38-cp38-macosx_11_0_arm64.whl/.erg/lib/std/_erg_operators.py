def __add__(x, y):
    return x + y
def __sub__(x, y):
    return x - y
def __mul__(x, y):
    return x * y
def __div__(x, y):
    return x / y
def __floordiv__(x, y):
    return x // y
def __mod__(x, y):
    return x % y
def __pow__(x, y):
    return x ** y
def __lshift__(x, y):
    return x << y
def __rshift__(x, y):
    return x >> y
def __and__(x, y):
    return x & y
def __or__(x, y):
    return x | y
def __xor__(x, y):
    return x ^ y
def __pos__(x):
    return +x
def __neg__(x):
    return -x
def __invert__(x):
    return ~x
